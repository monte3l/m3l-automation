#!/usr/bin/env node
// Generates docs/reference/catalog.json, docs/reference/symbol-map.json,
// and the generated table block in docs/reference/README.md.
// Run via: pnpm gen:index
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { format, resolveConfig } from "prettier";
import {
  BEGIN_MARKER,
  END_MARKER,
  SCRIPTS_BEGIN_MARKER,
  SCRIPTS_END_MARKER,
  buildIndex,
  buildReadmeBlock,
  buildScriptsCatalog,
  buildScriptsReadmeBlock,
  root,
} from "./lib/reference-index.mjs";

const refDir = join(root, "docs/reference");

// Write JSON the way prettier would, so `pnpm format` before or after
// `pnpm gen:index` never leaves `format:check` failing (the generator used to
// emit a non-prettier style — docs/logs/2026-07-01-core-analysis.md,
// divergence 6). Resolving config by filepath applies the repo's prettier
// settings and the json parser.
async function writeJsonFormatted(filePath, data) {
  const config = await resolveConfig(filePath);
  const formatted = await format(JSON.stringify(data, null, 2), {
    ...config,
    filepath: filePath,
  });
  writeFileSync(filePath, formatted, "utf8");
}

function replaceBlock(content, beginMarker, endMarker, block, heading) {
  const start = content.indexOf(beginMarker);
  const end = content.indexOf(endMarker);
  if (start !== -1 && end !== -1) {
    return (
      content.slice(0, start) + block + content.slice(end + endMarker.length)
    );
  }
  const lead = heading ? `\n\n${heading}\n\n` : "\n\n";
  return content.trimEnd() + lead + block + "\n";
}

function updateReadme(catalog, scriptsCatalog) {
  const readmePath = join(refDir, "README.md");
  const block = buildReadmeBlock(catalog);
  const scriptsBlock = buildScriptsReadmeBlock(scriptsCatalog);
  let content;
  try {
    content = readFileSync(readmePath, "utf8");
  } catch {
    content = [
      "# Reference index",
      "",
      "A-Z index of all `@m3l-automation/m3l-common` submodules.",
      "Generated by `pnpm gen:index` — do not edit the block below manually.",
      "",
      block,
      "",
    ].join("\n");
  }
  content = replaceBlock(content, BEGIN_MARKER, END_MARKER, block);
  content = replaceBlock(
    content,
    SCRIPTS_BEGIN_MARKER,
    SCRIPTS_END_MARKER,
    scriptsBlock,
    "## Consumer scripts",
  );
  writeFileSync(readmePath, content, "utf8");
}

const { catalog, symbolMap } = buildIndex();
const scriptsCatalog = buildScriptsCatalog();

mkdirSync(refDir, { recursive: true });

await writeJsonFormatted(join(refDir, "catalog.json"), catalog);
await writeJsonFormatted(join(refDir, "symbol-map.json"), symbolMap);
updateReadme(catalog, scriptsCatalog);

console.log(
  `✓  Reference index generated: ${catalog.length} modules, ${Object.keys(symbolMap).length} symbols, ${scriptsCatalog.length} consumer script(s).`,
);
