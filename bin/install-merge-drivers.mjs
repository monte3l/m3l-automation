#!/usr/bin/env node
// Registers the m3l-generated custom merge driver (bin/merge-driver-generated.mjs)
// into the SHARED repo config — `git config` without `--worktree`/`--local`
// path overrides writes to the common `.git/config`, which every linked
// worktree reads, so one registration covers all of them.
//
// Idempotent: re-running is a no-op once the driver/name are already set to
// the expected values. Wired into both `package.json`'s `prepare` script
// (covers a fresh clone) and `bin/worktree-setup.mjs` (covers a worktree
// whose `prepare` step was skipped, or that predates this installer).
//
// Graceful degradation: if this never runs (e.g. a fresh clone before
// `pnpm install`), the driver is simply unregistered and git falls back to a
// normal merge conflict on the .gitattributes-tagged files — nothing breaks.
//
// Usage:
//   node bin/install-merge-drivers.mjs
import process from "node:process";
import { execFileSync } from "node:child_process";

/**
 * The driver config git expects: the command line (fixed — forward slashes
 * and `node`, no quoting hazards on Windows since git runs drivers under its
 * own sh) and a human-readable name.
 *
 * @returns {{ driver: string, name: string }}
 */
export function mergeDriverConfig() {
  return {
    driver: "node bin/merge-driver-generated.mjs %O %A %B %P",
    name: "m3l-automation generated-file merge driver (keeps the current side; regenerated by the post-rewrite/post-merge hook)",
  };
}

/**
 * Default git runner; returns trimmed stdout. Injectable for tests.
 *
 * @param {string[]} args
 * @returns {string}
 */
function defaultRunGit(args) {
  return execFileSync("git", args, { encoding: "utf8" }).trim();
}

/**
 * Read a git config key, or `null` if it is unset (a non-zero exit from
 * `git config --get` on a missing key, not an error condition here).
 *
 * @param {(args: string[]) => string} runGit
 * @param {string} key
 * @returns {string | null}
 */
function readConfig(runGit, key) {
  try {
    return runGit(["config", "--get", key]);
  } catch {
    return null;
  }
}

/**
 * Idempotently write the `merge.m3l-generated.driver`/`.name` config keys.
 * Only issues a `git config` write for a key whose current value differs
 * from the expected one, so a repeat run is a true no-op.
 *
 * @param {(args: string[]) => string} [runGit]
 * @returns {{ driver: "set" | "unchanged", name: "set" | "unchanged" }}
 */
export function installMergeDrivers(runGit = defaultRunGit) {
  const { driver, name } = mergeDriverConfig();
  const result = { driver: "unchanged", name: "unchanged" };

  if (readConfig(runGit, "merge.m3l-generated.driver") !== driver) {
    runGit(["config", "merge.m3l-generated.driver", driver]);
    result.driver = "set";
  }
  if (readConfig(runGit, "merge.m3l-generated.name") !== name) {
    runGit(["config", "merge.m3l-generated.name", name]);
    result.name = "set";
  }

  return result;
}

if (process.argv[1]?.endsWith("install-merge-drivers.mjs")) {
  const result = installMergeDrivers();
  const changed = Object.entries(result)
    .filter(([, v]) => v === "set")
    .map(([k]) => k);
  console.log(
    changed.length > 0
      ? `✓  merge.m3l-generated config: ${changed.join(", ")} (${changed.length}/2 key(s) written).`
      : "✓  merge.m3l-generated config already up to date.",
  );
}
